    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        // TODO add your handling code here:
        try {
    // Mostrar cursor de espera
    this.setCursor(new java.awt.Cursor(java.awt.Cursor.WAIT_CURSOR));
    
    // Limpiar la tabla
    DefaultTableModel modelo = (DefaultTableModel) tblTurnos.getModel();
    modelo.setRowCount(0);

    // Obtener lista de turnos
    List<Turnos> turnos = TurnosController.obtenerTurnos();
    
    if (turnos != null && !turnos.isEmpty()) {
        // Agregar cada turno a la tabla
        for (Turnos turno : turnos) {
            Object[] fila = {
                turno.getId(),
                turno.getFecha(),
                turno.getClienteNombre(),
                turno.getServicio(),
                turno.getEstado()
            };
            modelo.addRow(fila);
        }
    } else {
        JOptionPane.showMessageDialog(
            this,
            "No se encontraron turnos registrados",
            "Informaci√≥n",
            JOptionPane.INFORMATION_MESSAGE
        );
    }

} catch (Exception ex) {
    JOptionPane.showMessageDialog(
        this,
        "Error al consultar los turnos: " + ex.getMessage(),
        "Error",
        JOptionPane.ERROR_MESSAGE
    );
} finally {
    // Restaurar cursor normal
    this.setCursor(new java.awt.Cursor(java.awt.Cursor.DEFAULT_CURSOR));
}
    }//GEN-LAST:event_btnConsultarActionPerformed

