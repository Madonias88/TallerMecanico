    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        // TODO add your handling code here:
        System.out.println("Bot√≥n CONSULTAR presionado");
    // Obtener los servicios de la API
    List<Servicios> servicios = ServicesController.obtenerServicios();
    
    if (servicios != null) {
        // Obtener el modelo de la tabla
        DefaultTableModel modelo = (DefaultTableModel) tblServices.getModel();
        
        // Limpiar la tabla antes de agregar nuevos datos
        modelo.setRowCount(0);
        
        // Agregar los datos a la tabla
        for (Servicios servicio : servicios) {
            Object[] fila = new Object[]{
                servicio.getId(),
                servicio.getNombre(),
                servicio.getDescripcion(),
                servicio.getPrecio(),
                servicio.getDuracionMinutos()
            };
            modelo.addRow(fila);
        }
    } else {
        // Mostrar mensaje de error si no se pudieron obtener los datos
        JOptionPane.showMessageDialog(this, 
            "Error al obtener los servicios", 
            "Error", 
            JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnConsultarActionPerformed

