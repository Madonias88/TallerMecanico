    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        // TODO add your handling code here:
        try {
            // Limpiar la tabla
DefaultTableModel modeloTabla = (DefaultTableModel) tblRepuestos.getModel();

            modeloTabla.setRowCount(0);
            
            // Obtener datos
            List<Repuestos> listaRepuestos = RepuestosController.obtenerRepuestos();
            
            if (listaRepuestos != null && !listaRepuestos.isEmpty()) {
                // Llenar la tabla con los datos
                for (Repuestos repuesto : listaRepuestos) {
                    Object[] fila = new Object[]{
                        repuesto.getId(),
                        repuesto.getNombre(),
                        repuesto.getMarca(),
                        repuesto.getPrecio(),
                        repuesto.getStock()
                    };
                    modeloTabla.addRow(fila);
                }
            } else {
                JOptionPane.showMessageDialog(this,
                    "No se encontraron repuestos",
                    "Informaci√≥n",
                    JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,
                "Error al consultar los repuestos: " + e.getMessage(),
                "Error",
                JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    
    }//GEN-LAST:event_btnConsultarActionPerformed

